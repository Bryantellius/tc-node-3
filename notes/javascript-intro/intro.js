// Loops

let continueLoopWhileAbove0 = 10;

// While
// Will loop 0 or more times

while (continueLoopWhileAbove0) {
  console.log(continueLoopWhileAbove0);
  continueLoopWhileAbove0--;
}

// Do/While
// Will loop 1 or more times

do {
  console.log(continueLoopWhileAbove0);
  continueLoopWhileAbove0--;
} while (continueLoopWhileAbove0);

// For

for (
  let continueLoopWhileAbove0 = 10;
  continueLoopWhileAbove0;
  continueLoopWhileAbove0--
) {
  console.log(continueLoopWhileAbove0);
}
